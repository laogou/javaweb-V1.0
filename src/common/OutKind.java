package common;

public class OutKind {
	private String outkindcode="";    //出账类型编码
	
	private String outkindname="";    //出账类型名字
	
	public String getOutkindcode() {
		return outkindcode;
	}
	public void setOutkindcode(String outkindcode) {
		this.outkindcode = outkindcode;
	}
	public String getOutkindname() {
		return outkindname;
	}
	public void setOutkindname(String outkindname) {
		this.outkindname = outkindname;
	}
}
